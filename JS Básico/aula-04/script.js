//Operadores aritméticos

//Adição +
var sumTwoNumbers = 1 + 3
console.log(sumTwoNumbers)

//Subtração -
var subTwoNumbers = 3 - 1
console.log(subTwoNumbers)

//Multiplicação *
var multTwoNumbers = 5 * 5
console.log(multTwoNumbers)

//Divisão /
var divTwoNumbers = 10 / 5
console.log(divTwoNumbers)

//Módulo ou resto de divisão %
var modTwoNumbers = 5 % 2
console.log(modTwoNumbers)

//Incremento ++
var num = 1
console.log(++num)
//num = num + 1

//Decremento --
var num = 2
console.log(--num)
//num = num - 1

//Operadores de atribuição
//= atribuição

//atribuição de soma
var x = 1
var y = 3

console.log(x+=y) //== x = x + y
console.log(x-=y) //== x = x - y
console.log(x*=y) //== x = x * y
console.log(x/=y) //== x = x / y
console.log(x%=y) //== x = x % y

//Operadores de comparação
