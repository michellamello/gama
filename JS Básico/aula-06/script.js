var colors = ["black", "white", "yellow", "blue", "green"];

//for

for (var i = 0; i < colors.length; i++) {
    console.log(colors[i]);
}

for (var i = 1; i <= 10; i++) {
    console.log(i);
}

//while

var i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}

//do while

var i = 1

do {
    console.log(i);
    i++;
} while (i <= 10)